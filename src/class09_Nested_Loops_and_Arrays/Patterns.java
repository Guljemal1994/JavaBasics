package class09_Nested_Loops_and_Arrays;

public class Patterns {

	public static void main(String[] args) {
		//*****
		for(int a=1; a<=5; a++) {
		System.out.println("*");
		
		}
		
		
	System.out.println("-----Printing Pattern using nexted Loops--");
		for(int i=1; i<=4; i++) {
		for(int j=1; j<=5; j++) {
			System.out.print("*");
		
	}
		System.out.println("*");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
