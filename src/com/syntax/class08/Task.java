package com.syntax.class08;

public class Task {

	public static void main(String[] args) {
		
		int sum=0;
		for(int g=1; g<=99; g+=2) {
			sum=sum+g;
			
			
		}
		
		System.out.println("Odd numbers "+sum);
		
		
		int sum1=0;
		for(int k=1; k<=99; k++) {
			
			if(k%2==1) {
				sum1=sum1+k;
			}
		}
		
		System.out.println("Even numbers "+sum1);
		
		
		System.out.println("*******************");
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
