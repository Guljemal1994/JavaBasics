package class03_Casing_and_If_Condition;

public class ShorthandOperation {

	public static void main(String [] args) {
		
	int num=200;
	num=num+300;
	System.out.println(num);//500
	
	num=num-100;
	System.out.println(num);//400
    
	//compoud shorthand assignment operation 
	
	num-=100; //or num=num-100
	System.out.println(num);
	
	num+=50; //or num=num+50
	System.out.println(num);//350
	
	num/=7; 
	System.out.println(num);//50
	
	num*=10;
	System.out.println(num);//500
	
	num%=5;
	System.out.println(num);//0
	
	
	
		
		
		
	}
}
