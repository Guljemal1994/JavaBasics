package class03_Casing_and_If_Condition;

public class TemperatureCheck {

public static void main(String[] args) {
	
	int gulTemp = 20;
	int gulTemp1 =30;
	
	if(gulTemp<gulTemp1) {
		System.out.println("Water will freeze "+20);
		
	}else {
		System.out.println("Water will not freeze "+30);
	}
	
	int temp =40;
	int freezeTemp =32;
	
	if(temp<freezeTemp) {
		System.out.println("Water will freeze "+temp);
		
	}else {
		System.out.println("Water will not freeze "+ temp);
	
	}
}

}


