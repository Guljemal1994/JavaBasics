package class22_Overriding_Polymorphism;

public class Recap {

	public void hello() {
		System.out.println("Hello");
	}
	public void hello(String name ) {//this is overloaded
		System.out.println("hello"+name);
	}
	public void hello(int num ) {//this is overloaded
		System.out.println("hello"+num);
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
